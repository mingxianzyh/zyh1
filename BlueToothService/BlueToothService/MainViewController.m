@interface MainViewController ()

@property (nonatomic,strong) CBPeripheralManager *manage;

@property (nonatomic,copy) NSString *serviceUUID;

@property (nonatomic,copy) NSString *characteristicUUID;

@end

@implementation MainViewController

- (id)initWithNibName:(NSString *)nibNameOrNil bundle:(NSBundle *)nibBundleOrNil
{
    self = [super initWithNibName:nibNameOrNil bundle:nibBundleOrNil];
    if (self) {
        // Custom initialization
    }
    return self;
}

- (void)viewDidLoad
{
    [super viewDidLoad];
    // Do any additional setup after loading the view.
    //1.初始化后调用代理方法peripheralManagerDidUpdateState:
    self.manage = [[CBPeripheralManager alloc] initWithDelegate:self queue:nil options:@{CBPeripheralManagerOptionRestoreIdentifierKey:@"peripheral"}];
    
}

- (void)start{
    //3.为管理类增加服务
    self.serviceUUID = [[NSUUID UUID] UUIDString];
    self.characteristicUUID = [[NSUUID UUID] UUIDString];
    //创建特征
    CBUUID *characterUUID = [CBUUID UUIDWithString:self.characteristicUUID];
    CBMutableCharacteristic *character = [[CBMutableCharacteristic alloc] initWithType:characterUUID properties:CBCharacteristicPropertyNotify value:nil permissions:CBAttributePermissionsReadable];
    CBUUID *serviceUUID = [CBUUID UUIDWithString:self.serviceUUID];
    //创建服务
    CBMutableService *service = [[CBMutableService alloc] initWithType:serviceUUID primary:YES];
    [service setCharacteristics:@[character]];
    //将服务加在周边设备管理类上
    [self.manage addService:service];
}

#pragma mark 周边设备代理
- (void)peripheralManagerDidUpdateState:(CBPeripheralManager *)peripheral{
    //2.判断状态
    NSLog(@"%d",peripheral.state);
    switch (peripheral.state) {
        case CBPeripheralManagerStatePoweredOn:{
            [self start];
        }
            break;
            
        default:
            break;
    }
    

}

